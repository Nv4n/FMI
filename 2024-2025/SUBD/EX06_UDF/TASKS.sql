-- T01
CREATE OR REPLACE FUNCTION EMP_AGE(EMP_NO INT)
    RETURNS INT
    SPECIFIC EMP_AGE
    RETURN SELECT YEAR(CURRENT_DATE - BIRTHDATE) AS AGE
           FROM DB2SAMPLE.EMP
           WHERE EMPNO = EMP_NO;


-- T02
CREATE OR REPLACE FUNCTION EMP_EXPERIENCE(EMP_NO INT)
    RETURNS INT
    SPECIFIC EMP_EXPERIENCE
    RETURN SELECT YEAR(CURRENT_DATE - HIREDATE) AS EXPERIENCE
           FROM DB2SAMPLE.EMP
           WHERE EMPNO = EMP_NO;

